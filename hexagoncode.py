def hexagon(turta, hexa_color, border_color):
    turta.color(border_color, hexa_color)
    turta.begin_fill()

    turta.forward(50)
    turta.left(60)

    turta.forward(50)
    turta.left(60)

    turta.forward(50)
    turta.left(60)

    turta.forward(50)
    turta.left(60)

    turta.forward(50)
    turta.left(60)

    turta.forward(50)
    turta.left(60)

    turta.end_fill()
