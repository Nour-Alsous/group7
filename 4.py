def square(turta, square_color, border_color):
    turta.color(border_color, square_color)
    turta.begin_fill()

    turta.forward(90)
    turta.left(90)

    turta.forward(90)
    turta.left(90)

    turta.forward(90)
    turta.left(90)

    turta.forward(90)
    turta.left(90)

    turta.end_fill()
    